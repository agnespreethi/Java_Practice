public class DogSetter {
    private int legs;
    private boolean tail;
    private String hair;

    public DogSetter(){

    }
    public int getLegs(){
        return legs;
    }
    public void setLegs(int legs){
        this.legs = legs;
    }
    
    
    public boolean getTail(){
        return tail;
    }
    public void setTail(boolean tail){
        this.tail = tail;
    }

    public String getHair(){
        return hair;
    }
    public void setHair(String hair){
        this.hair = hair;
    }
}
